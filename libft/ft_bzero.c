#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*d;
	size_t	i;

	i = 0;
	if (n != 0)
	{
		d = (char *)s;
		while (i < n)
		{
			d[i] = 0;
			i++;
		}
	}
}
